package github.com.miguelfreelancer56577.hackerrank;
/**
 * This class contains the solution for the problem
 * @see <a href="https://www.hackerrank.com/challenges/solve-me-first/problem">Solve Me First</a>
 * 
 * @author mangelt
 *
 */
public class SolveMeFirst {

	public static void main(String[] args) {
		int a = 2;
		int b = 3;
		System.out.println(solution(a, b));
	}
	
	/**
	 * It computes the sum of two integers
	 * @param a {@literal Integer} with the first number
	 * @param b {@literal Integer} with the second number
	 * 
	 */
	public static int solution(int a, int b){
		return a+b;
	}

}
